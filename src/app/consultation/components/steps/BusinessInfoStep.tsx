export default function BusinessInfoStep({
  formData,
  onNext,
  updateFormData
}: BusinessInfoStepProps) {
  const [errors, setErrors] = useState<Record<string, string>>({});

  const validateForm = () => {
    const newErrors: Record<string, string> = {};

    if (!formData.businessName.trim()) {
      newErrors.businessName = 'Business name is required';
    }
    if (!formData.industry) {
      newErrors.industry = 'Please select an industry';
    }
    if (!formData.businessSize) {
      newErrors.businessSize = 'Please select a business size';
    }

    setErrors(newErrors);
    return Object.keys(newErrors).length === 0;
  };

  const handleSubmit = () => {
    if (validateForm()) {
      onNext();
    }
  };

  const handleInputChange = (field: keyof FormData, value: string) => {
    updateFormData({ [field]: value });
    if (errors[field]) {
      setErrors(prev => ({
        ...prev,
        [field]: ''
      }));
    }
  };

  return (
    <motion.div
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      exit={{ opacity: 0, y: -20 }}
      className={formStyles.section}
    >
      <div className={formStyles.header.wrapper}>
        <h2 className={formStyles.header.title}>
          Tell us about your business
        </h2>
        <p className={formStyles.header.description}>
          Help us understand your business better so we can create the perfect solution for you.
        </p>
      </div>

      <div className={formStyles.field.wrapper}>
        <div className="space-y-4">
          <div>
            <label htmlFor="businessName" className={formStyles.field.label}>
              Business Name
            </label>
            <input
              type="text"
              id="businessName"
              value={formData.businessName}
              onChange={(e) => handleInputChange('businessName', e.target.value)}
              className={formStyles.field.input}
              placeholder="Enter your business name"
            />
            {errors.businessName && (
              <p className={formStyles.field.error}>{errors.businessName}</p>
            )}
          </div>

          <div>
            <label htmlFor="currentWebsite" className={formStyles.field.label}>
              Current Website (if any)
            </label>
            <input
              type="url"
              id="currentWebsite"
              value={formData.currentWebsite}
              onChange={(e) => handleInputChange('currentWebsite', e.target.value)}
              className={formStyles.field.input}
              placeholder="https://example.com"
            />
          </div>
        </div>

        <div>
          <label className={formStyles.field.label}>
            Industry
          </label>
          <div className={formStyles.grid.cols['4']}>
            {industries.map(industry => (
              <button
                key={industry.id}
                type="button"
                onClick={() => handleInputChange('industry', industry.id)}
                className={`${formStyles.card.base} ${
                  formData.industry === industry.id
                    ? formStyles.card.selected
                    : formStyles.card.unselected
                }`}
              >
                <div className={`${formStyles.card.icon.base} ${
                  formData.industry === industry.id 
                    ? formStyles.card.icon.selected 
                    : formStyles.card.icon.unselected
                }`}>
                  {industry.icon}
                </div>
                <span className="text-sm font-medium">{industry.label}</span>
              </button>
            ))}
          </div>
          {errors.industry && (
            <p className={formStyles.field.error}>{errors.industry}</p>
          )}
        </div>

        <div>
          <label className={formStyles.field.label}>
            Business Size
          </label>
          <div className={formStyles.grid.cols['2']}>
            {businessSizes.map((size) => (
              <button
                key={size.id}
                type="button"
                onClick={() => handleInputChange('businessSize', size.id)}
                className={`${formStyles.card.base} ${
                  formData.businessSize === size.id
                    ? formStyles.card.selected
                    : formStyles.card.unselected
                }`}
              >
                <div className={`${formStyles.card.icon.base} ${
                  formData.businessSize === size.id 
                    ? formStyles.card.icon.selected 
                    : formStyles.card.icon.unselected
                }`}>
                  {size.icon}
                </div>
                <div className="flex flex-col">
                  <span className="text-sm font-medium">{size.label}</span>
                  <span className="mt-1 text-xs text-gray-500">{size.description}</span>
                </div>
              </button>
            ))}
          </div>
          {errors.businessSize && (
            <p className={formStyles.field.error}>{errors.businessSize}</p>
          )}
        </div>
      </div>

      <div className="flex justify-end pt-6">
        <button
          type="button"
          onClick={handleSubmit}
          className={`${formStyles.button.base} ${formStyles.button.primary}`}
        >
          Next Step
          <svg
            className="ml-2 -mr-1 h-5 w-5"
            fill="none"
            viewBox="0 0 24 24"
            stroke="currentColor"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M9 5l7 7-7 7"
            />
          </svg>
        </button>
      </div>
    </motion.div>
  );
} 